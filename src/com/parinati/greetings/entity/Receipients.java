package com.parinati.greetings.entity;

public class Receipients {

	private int recptId;
	private String recptTitle;
	private String recptName;
	private String recptEmail;

	public int getRecptId() {
		return recptId;
	}

	public void setRecptId(int recptId) {
		this.recptId = recptId;
	}

	public String getRecptTitle() {
		return recptTitle;
	}

	public void setRecptTitle(String recptTitle) {
		this.recptTitle = recptTitle;
	}

	public String getRecptName() {
		return recptName;
	}

	public void setRecptName(String recptName) {
		this.recptName = recptName;
	}

	public String getRecptEmail() {
		return recptEmail;
	}

	public void setRecptEmail(String recptEmail) {
		this.recptEmail = recptEmail;
	}

}
